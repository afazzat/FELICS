#include <stdint.h>

#include "cipher.h"
#include "constants.h"


void RunDecryptionKeySchedule(uint8_t *key, uint8_t *roundKeys)
{
	uint8_t i, j;
	memcpy((void*)roundKeys,(void*)key,KEY_SIZE);
	for(i=0;i<(NUMBER_OF_ROUNDS);i++) memcpy((void*)(roundKeys+KEY_SIZE+8*i),(void*)(key+8),BLOCK_SIZE);
	for(i=0;i<NUMBER_OF_ROUNDS;i++){
		for(j=0;j<4;j++) roundKeys[8*(i+1)+2*j+1] += READ_ROUND_CONSTANT_BYTE(round_constants[76-4*i+j]);
	}
}

